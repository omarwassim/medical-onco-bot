def clean_and_filter_keywords(raw_keywords, valid_keys):
    cleaned = []
    for kw in raw_keywords:
        kw = kw.strip()
        kw = re.sub(r"[\[\]\"`]", "", kw)
        kw = kw.strip()
        if kw and kw in valid_keys:
            cleaned.append(kw)
    return cleaned
def detect_symptoms_pipeline(text):
    detected = set()
    text_lower = text.lower().replace("ـ", "").strip()
    # 1️⃣ استخراج الأعراض الحرفية مباشرة من النص العربي
    for key, synonyms in SYMPTOM_SYNONYMS.items():
        for syn in synonyms:
            if syn in text_lower:
                detected.add(key)
    # 2️⃣ تقسيم الجمل
    clauses = re.split(r"[,؟!.؛]| وكمان | وانا | لكن ", text_lower)
    clauses = [cl.strip() for cl in clauses if cl.strip()]
    for clause in clauses:
        # 3️⃣ ترجمة الجملة
        clause_en = translator.translate(clause, src='ar', dest='en').text
        prompt = f"""
        You are a medical assistant.
        Patient says: "{clause_en}"
        Detect any symptoms and output JSON array of strings, ONLY using the keys:
        {list(SYMPTOM_SYNONYMS.keys())}
        Respond ONLY with a JSON array, no extra text.
        """
        response = deepseek_chat(prompt)
        # 4️⃣ تنظيف واسترجاع النتائج
        try:
            raw_keywords = json.loads(response)
        except:
            raw_keywords = re.split(r"[,\n]", response)
        keywords = clean_and_filter_keywords(raw_keywords, SYMPTOM_KEYS)
        # 5️⃣ ضم كل الأعراض المكتشفة بدون شرط الـ synonym الصارم
        for kw in keywords:
            if kw in SYMPTOM_KEYS:
                detected.add(kw)
    return list(detected)
